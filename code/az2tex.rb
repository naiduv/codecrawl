#!/usr/local/bin/ruby -Ks
# 青空文庫→TeX（ver. 0.9.5 2004/5/5 psitau）

require 'jcode'
require 'kconv'

PreambleLineNumber=13
MidashiType = { "小" => "ko",  "中" => "naka",  "大" => "oo" }
BoutenType = {
   "傍点" => "bou", "白ゴマ傍点" => "sirogomabou", "丸傍点" => "marubou",
   "白丸傍点" => "siromarubou", "黒三角傍点" => "kurosankakubou",
   "白三角傍点" => "sirosankakubou", "二重丸傍点" => "nijyuumarubou", "蛇の目傍点" => "jyanomebou"
}

$log_text = []
$line_num=0

# 
# ハッシュを作成
$gaiji = {}

# メインパート
def main
   meta_data = []
   empty_line = 0
   in_note = false
   block_jisage = false
   block_jituki = false
   retsage = false

   while gaiji_line = DATA.gets
      gaiji_line.chomp!
      key, data = gaiji_line.split
      $gaiji[key] = data
   end

   # 入出力ファイルの定義
   inputfile_name = ARGV.shift
   outputfile_name = inputfile_name.sub(/\.txt$/, ".tex")
   begin
       inputfile = open(inputfile_name)
   rescue
       inputfile_name += ".txt"
       inputfile = open(inputfile_name)
       outputfile_name = inputfile_name.sub(/\.txt$/, ".tex")
   end
   outputfile = open(outputfile_name, "w")

   # プリアンブルの処理
   outputfile.print write_preamble()

   # 文字サイズ、段組みのオプション
   require 'optparse'
   charsize = "normal"
   kindle = "kindle2"
   OptionParser.new{|opt|
      opt.on('-s [VAL]') {|v| charsize = v if(v) }
      opt.on('-d [VAL]') {|v| kindle = "kindledx" if(v) }
   }.parse!(ARGV)
   outputfile.puts "\\usepackage[device=#{kindle},size=#{charsize}]{aozora}"

   count = 0
   while empty_line < 2
       count += 1
       line = inputfile.gets
       if(!line or count > 50)
           # 否青空分形式
           meta_data = []
           inputfile = open(inputfile_name)
           break
       end
       line = line.chop
       if in_note
           if line =~ /^-+$/ then in_note = false end
       else
           if line =~ /^-+$/
               in_note = true
           else
               if line =~ /^$/
                   empty_line += 1
               else
                   meta_data << translate_gaiji(line.gsub(/《[^》]+》/, ''))
               end
           end
       end
   end

   use_title = false
   if(meta_data.size > 0)
      use_title = true
      $line_num +=  meta_data.size
      outputfile.print "\\title{"+normalize(meta_data.shift)+"}\n"
      case meta_data.size
      when 1
         outputfile.print "\\author{"+normalize(meta_data.shift)+"}\n"
      when 2
         outputfile.print "\\subtitle{"+normalize(meta_data.shift)+"}\n"
         outputfile.print "\\author{"+normalize(meta_data.shift)+"}\n"
      when 3
         outputfile.print "\\subtitle{"+normalize(meta_data.shift)+"}\n"
         outputfile.print "\\author{"+normalize(meta_data.shift)+"}\n"
         outputfile.print "\\subauthor{"+normalize(meta_data.shift)+"}\n"
      else
         outputfile.print "\\subtitle{"+normalize(meta_data.shift)+"}\n"
         outputfile.print "\\author{"+normalize(meta_data.shift)+"}\n"
         outputfile.print "\\subauthor{"+normalize(meta_data.shift)+"}\n"
         until meta_data.empty?
            outputfile.print "\\metadata{"+normalize(meta_data.shift)+"}\n"
         end
      end
   end

   outputfile.print "\\date{}\n"

   # 本文の処理
   outputfile.print "\\begin{document}\n"
   outputfile.print "\\maketitle\n" if(use_title)

   $line_num += PreambleLineNumber

   while line = inputfile.gets
       $line_num += 1
       line.chop!
       break if line =~ /^底本/

       line.gsub!(/[\\%{}]/, '\\\\\&')

       if line =~ /^　「/
           line.sub!(/^　「/, "\\mbox{　}\\kern0mm\\inhibitglue「")
       end
       if line =~ /[ワヰヱヲ]゛/
           line.gsub!(/ワ゛/, "{\\ajLig{ワ゛}}")
           line.gsub!(/ヰ゛/, "{\\ajLig{ヰ゛}}")
           line.gsub!(/ヱ゛/, "{\\ajLig{ヱ゛}}")
           line.gsub!(/ヲ゛/, "{\\ajLig{ヲ゛}}")
       end
       if line =~ /[？！]　/
           line.gsub!(/([？！])　/, '\1{}')
       end
       if line =~ /――/
           line.gsub!(/――/, "\\――{}")
       end
       if line =~ /／＼/
           line.gsub!(/／＼/, "\\ajKunoji{}")
       end
       if line =~ /／″＼/
           line.gsub!(/／″＼/, "\\ajDKunoji{}")
       end
=begin
       if line =~ /^　　+.+/
           line.gsub!(/^　　+([一二三四五六七八九〇十].*)/, '\\section*{\1}')
       end
=end
       while line =~ /(.+?)［＃(「\1」は横?[１|一]文字[^］]*?)］/
               line = line.sub(/(.+?)［＃(「\1」は横?[１|一]文字[^］]*?)］/){"{\\ajLig{"+to_single_byte($1)+"}}"}
       end
       if line =~ /［＃改丁.*?］/
           line.sub!(/［＃改丁.*?］/, "\\cleardoublepage")
       end
       if line =~ /［＃改[頁|ページ].*?］/
           line.sub!(/［＃改[頁|ページ].*?］/, "\\clearpage")
       end
       if line =~ /※/
           translate_gaiji(line)
       end
       if line =~ /［＃(.+?)?傍点］/
           translate_bouten(line)
       end
       if line =~ /《.*?》/
           translate_ruby(line)
       end
       if line =~ /［＃「(.+?)」に(?:二重)?[傍鎖破波]線］/
           translate_bousen(line)
       end
       if line =~ /［＃.*?字下げ.*?(?:終わり|まで).*?］/ 
           line = line.sub(/［＃.*?字下げ.*?(?:終わり|まで).*?］/, "")+"\\end{jisage}"
           block_jisage = false
           retsage = false
       end
       if line =~ /［＃ここから.+字下げ.*?］/
           if block_jisage
               outputfile.print "\\end{jisage}\n"
               $line_num += 1
               block_jisage = false
           end
           num = 0
           line.sub!(/［＃ここから(?:(?:([１２３４５６７８９０一二三四五六七八九〇十]*)字下げ)|改行天付き)(.*)?］/) {
              num = to_single_byte($1).to_i
              "\\begin{jisage}{#{num}}"
           }
           if $2 =~ /折り返して([１２３４５６７８９０一二三四五六七八九〇十]*)字下げ/
              sage = to_single_byte($1).to_i - num
              retsage = "\\hangindent#{sage}zw" if(sage > 0)
           else
              retsage = false
           end
           block_jisage = true
       end
       if line =~ /［＃(?:この行)?.*?([１２３４５６７８９０一二三四五六七八九〇十]*)字下げ］/
           outputfile.print "\\begin{jisage}{"+to_single_byte($1)+"}\n"
           line = line.sub(/［＃(?:この行)?.*?字下げ］/, "")+"\n\\end{jisage}"
           $line_num += 2
       end
       # 地付き・地寄せ
       if line =~ /［＃ここで(?:地付き|字上げ)終わり］/
           line = line.sub(/［＃ここで(?:地付き|字上げ)終わり］/, "")+"\\end{flushright}"
           block_jituki = false
       end
       if line =~ /［＃(.*)?地付き.*?］/
           if block_jituki
               outputfile.print "\\end{flushright}\n"
               $line_num += 1
               block_jituki = false
           end
           if($1 == 'ここから')
              block_jituki = true
              line = line.sub(/［＃.*?地付き.*?］/, "\\begin{flushright}\n")
           else
              if line =~ /［＃.*?地付き.*?］$/
                 line = "\\begin{flushright}\n"+line.sub(/［＃.*?地付き.*?］$/, "\\end{flushright}")
              else
                 line = line.sub(/［＃.*?地付き.*?］/, "\\begin{flushright}") + "\\end{flushright}"
              end
           end
           $line_num += 1
       end
       if line =~ /［＃(.*)?(?:行末|地)(?:から|より).*?[１２３４５６７８９０一二三四五六七八九〇十]*字上.*?］/
           if block_jituki
               outputfile.print "\\end{flushright}\n"
               $line_num += 1
               block_jituki = false
           end
           if($1 == 'ここから')
              block_jituki = true
              line = line.sub(/［＃.*?(?:行末|地)(?:から|より)(.*?)字上.*?］/){
                 "\\begin{flushright}\\advance\\rightskip" + to_single_byte($1)+"zw\n" }
           else
              if line =~ /［＃.*?(?:行末|地)(?:から|より).*?([１２３４５６７８９０一二三四五六七八九〇十]*)字上.*?］$/
                 line = "\\begin{flushright}\\advance\\rightskip" + to_single_byte($1) + "zw\n" +
                        line.sub(/［＃.*?(?:行末|地)(?:から|より).*?字上.*?］$/, "\\end{flushright}")
              else
                 line = line.sub(/［＃.*?(?:行末|地)(?:から|より)(.*?)字上.*?］/){
                    "\\begin{flushright}\\advance\\rightskip" + to_single_byte($1)+"zw\n" } + "\\end{flushright}"
              end
           end
           $line_num += 1
       end

       if line =~ /［＃「.+?」は返り点］/
           line.gsub!(/(.+)［＃「\1」は返り点］/, '')
           #line.gsub!(/(.+)［＃「\1」は返り点］/, '\\kaeriten{\ajKunten{\1}}')
           # TODO: 青空キンドルではエラーになる(青キン明朝の影響？)
       end
       if line =~ /［＃[一二上中下甲乙丙丁レ]*］/
           line.gsub!(/［＃([一二上中下甲乙丙丁レ]*)］/, '')
           #line.gsub!(/［＃([一二上中下甲乙丙丁レ]*)］/, '\\kaeriten{\ajKunten{\1}}')
           # TODO: 青空キンドルではエラーになる(同上)
       end
       if line =~ /［＃「(.+?)」は.見出し］/
           line.gsub!(/(.*)［＃「(#{$1})」は(.)見出し］/) {
               mtype = MidashiType[$3] || MidashiType["中"]
               "\\#{mtype}midashi{#{$1}}\\subtitle{#{$2}}"
           }
       end
       line.gsub!(/［＃大見出し］(.*?)［＃大見出し終わり］/, '\\oomidashi{\1}\\subtitle{\1}')
       line.gsub!(/［＃中見出し］(.*?)［＃中見出し終わり］/, '\\nakamidashi{\1}\\subtitle{\1}')
       line.gsub!(/［＃小見出し］(.*?)［＃小見出し終わり］/, '\\komidashi{\1}\\subtitle{\1}')

       # TODO: 青キン明朝では表示されない
       line.gsub!(/(.*?)［＃「\1」は太字］/, '\\azbold{\1}')
       line.gsub!(/［＃(?:ここから)?(?:太字|斜体)］/, '')
       line.gsub!(/［＃(?:ここで)?(?:太字|斜体)終わり］/, '')
       line.gsub!(/(.*?)［＃「\1」は斜体］/, '\\azitalic{\1}')
       # ここまで表示できない

       if line =~ /［＃（.*?）］/
           line.gsub!(/［＃（(.*?)）］/, '\\okurigana{\1}')
       end
       if line =~ /［＃「.+?」.*?ママ.*?注記］/
           line.gsub!(/(.+)［＃「\1」.*?ママ.*?注記］/, '\\ruby{\1}{ママ}')
       end

       # 挿絵
       line.gsub!(/［＃[^］]+（([^）]+.png)(?:、[^）]+)?）(?:[^］]+)?］/, '\\sashie{\1.eps}')
       #line.gsub!(/［＃[^］]+（([^）]+.png)(?:、[^）]+)?）(?:[^］]+)?］/, '\\sashie{\1}')

       # 割注
       line.gsub!(/［＃ここから割り注］(.*?)［＃ここで割り注終わり］/, '\\warichu{\1}')

       # 編集注
       line.gsub!(/［＃(「[^］]+?」はママ)］/, '\\footnote{\1}')
       line.gsub!(/［＃([^］]+?は底本では「[^］]+?」)］/, '\\footnote{\1}')

       # 後註
       line.gsub!(/［＃([^］]+?)］/, '\\endnote{\1}')

       if line =~ /\\[a-z]*?bou/
           tuning_bou(line)
       end
       if line =~ /\\ajD?Kunoji\{\}\}/
           line.gsub!(/(\\ajD?Kunoji)\{\}\}/, '\1}')
       end
       if line =~ /\\ruby/
           tuning_ruby(line)
       end
       if line =~ /^$/
           line = "　"
       end
       if block_jisage and retsage
          line = retsage + line
       end
       outputfile.print normalize(line)+"\n"
   end

   # 底本の処理
   outputfile.print write_postamble()
   outputfile.print normalize(line)+"\n"

   while line = inputfile.gets
      line.chop!
      outputfile.print normalize(line)+"\n"
   end

   outputfile.print "\n\\end{minipage}\n\\end{teihon}\n\\end{document}\n"

   if $log_text.size > 0
      until $log_text.empty?
         outputfile.print $log_text.shift
      end
   end
end

# メインパート終了 

# JISで出力
def normalize(l)
  Kconv::tojis(l.to_s)
end

# 全角→半角
def to_single_byte(s)
	if s =~ /[０-９]/
		s.tr!("１２３４５６７８９０", "1234567890")
	elsif s =~ /[一二三四五六七八九〇]/
		s.tr!("一二三四五六七八九〇", "1234567890")
	end
	case s
	when /\d十\d/
		s.sub!(/(\d)十(\d)/, '\1\2')
	when /\d十/
		s.sub!(/(\d)十/, '\{1}0')
	when /十\d/
		s.sub!(/十(\d)/, '1\1')
	when /十/
		s.sub!(/十/, "10")
	end
	if s =~/[！？]/
		s.tr!("！？", "!?")
	end
	return s
end

# プリアンブルの出力
def write_preamble
   return <<"END_OF_PRE"
\\documentclass[a5paper]{tbook}
%\\documentclass[a5paper, twocolumn]{tbook}
\\usepackage[deluxe]{otf}
%\\usepackage[expert, deluxe]{otf}
%##MyAUTHOR##
%\\usepackage{utf}
\\usepackage{furikana}
\\usepackage{type1cm}
\\def\\rubykatuji{\\rubyfamily\\tiny}
\\def\\rubykatuji{\\tiny}%for UTF package
%\\renewenvironment{teihon}{\\comment}{\\endcomment}
END_OF_PRE
end

# 底本の表示用
#
def write_postamble
   return <<"END_OF_POST"
\\theendnotes
\\begin{teihon}
\\clearpage\\null\\newpage\\thispagestyle{empty}
\\begin{minipage}<y>{\\textheight}
\\vspace{4\\baselineskip}
\\scriptsize
END_OF_POST
end

# 外字の処理用
def translate_gaiji(l)
    if l =~ /※［＃[^］]+（([^）]+.png)(?:、[^）]+)?）(?:[^］]+)］/
       l.gsub!(/※［＃[^］]+（([^）]+.png)(?:、[^）]+)?）(?:[^］]+)?］/, '\\exsashie{\1.eps}')
       #l.gsub!(/※［＃[^］]+（([^）]+.png)(?:、[^）]+)?）(?:[^］]+)?］/, '\\exsashie{\1}')
    end

	if l =~ /※［＃[^］]*?※［＃[^］]*?[12]\-\d{1,2}\-\d{1,2}[^］]*?］[^］]*?］/
		l.gsub!(/※［＃([^］]*?)※［＃([^］]*?([12]\-\d{1,2}\-\d{1,2})[^］]*?)］([^］]*?)］/){"※\\footnote{#$1"+$gaiji[$3]+"#$4}"}
	end
	if l =~ /※［＃[^］]*?[12]\-\d{1,2}\-\d{1,2}[^］]*?］/
		l.gsub!(/※［＃([^］]*?([12]\-\d{1,2}\-\d{1,2})[^］]*?)］/){$gaiji[$2]}
	end
	if l =~ /※［＃[^］]+?］/
		l.gsub!(/※［＃([^］]+?)］/, '※\\footnote{\1}')
	end
	if l =~ /※/
		STDERR.puts("Remaining Unprocessed Gaiji Character in Line #$line_num.")
		$log_text << normalize("未処理の外字が#{$line_num}行目にあります．\n")
	end
	return l
end

# ルビの処理用
def translate_ruby(l)
	# 被ルビ文字列内に外字の注記があるばあい，ルビ文字列の後ろに移動する
	# ただし，順番が入れ替わってしまう
	while l =~ /※\\footnote\{[^(?:\\footnote)]+\}(?:[亜-熙々※ヶ]|(?:\\UTF\{[0-9a-fA-F]+\}|\\CID\{[0-9]+\}))*?《.+?》/
		l.sub!(/(※)(\\footnote\{[^(?:\\footnote)]+\})((?:[亜-熙々※ヶ]|(?:\\UTF\{[0-9a-fA-F]+\}|\\CID\{[0-9]+\}))*?《.+?》)/, '\1\3\2')
	end
	# 被ルビ文字列内に誤記などの注記が存在する場合は、ルビの後ろに移動する
	while l =~ /(?:[亜-熙々※ヶ]|(?:\\UTF\{[0-9a-fA-F]+\}|\\CID\{[0-9]+\}))+?［＃[^］]*?］(?:[亜-熙々※ヶ]|(?:\\UTF\{[0-9a-fA-F]+\}|\\CID\{[0-9]+\}))*?《.+?》/
		l.sub!(/((?:[亜-熙々※ヶ]|(?:\\UTF\{[0-9a-fA-F]+\}|\\CID\{[0-9]+\}))+?)(［＃[^］]*?］)((?:[亜-熙々※ヶ]|(?:\\UTF\{[0-9a-fA-F]+\}|\\CID\{[0-9]+\}))*?《.+?》)/, '\1\3\2')
	end
	# ルビ文字列内に誤記などの注記が存在する場合は、ルビの後ろに移動する
	if l =~ /《[^》]*?［＃[^］]*?］[^》]*?》/
		l.gsub!(/(《[^》]*?)(［＃[^］]*?］)([^》]*?》)/, '\1\3\2')
	end
	# 一連のルビの処理
	# １ 縦棒ありの場合
	if l =~ /｜/
		l.gsub!(/｜(.+?)《(.+?)》/, '\\ruby{\1}{\2}')
	end
	# ２ 漢字および外字
	if l =~ /(?:[亜-熙々〆※ヶ]|(?:\\UTF\{[0-9a-fA-F]+\}|\\CID\{[0-9]+\}))+?《.+?》/
		l.gsub!(/((?:[亜-熙々〆※ヶ]|(?:\\UTF\{[0-9a-fA-F]+\}|\\CID\{[0-9]+\}))+?)《(.+?)》/, '\\ruby{\1}{\2}')
	end
	# ３ ひらがな
	if l =~ /[あ-ん](?:[ぁ-んーヽヾ]|\\CID\{12107\})*?《.+?》/
		l.gsub!(/([あ-ん](?:[ぁ-んーヽヾ]|\\CID\{12107\})*?)《(.+?)》/, '\\ruby{\1}{\2}')
	end
	# ４ カタカナ
	if l =~ /[ア-ヴ](?:[ァ-ヴーゝゞ]|\\CID\{12107\})*?《.+?》/
		l.gsub!(/([ア-ヴ](?:[ァ-ヴーゝゞ]|\\CID\{12107\})*?)《(.+?)》/, '\\ruby{\1}{\2}')
	end
	# ５ 全角アルファベットなど
	if l =~ /[Ａ-Ｚａ-ｚΑ-Ωα-ωА-Яа-я・]+?《.+?》/
		l.gsub!(/([Ａ-Ｚａ-ｚΑ-Ωα-ωА-Яа-я・]+?)《(.+?)》/, '\\ruby{\1}{\2}')
	end
	# ６ 半角英数字
	if l =~ /[A-Za-z0-9#\-\;\&. ]+?《.+?》/
		l.gsub!(/([A-Za-z0-9#\-\;\&. ]+?)《(.+?)》/, '\\ruby{\1}{\2}')
	end
	if l =~ /《.*?》/
		STDERR.puts("Unknown ruby pattern found in #$line_num.")
		$log_text << normalize("未処理のルビが#{$line_num}行目にあります．\n")
	end
	return l
end

# 傍点の処理用
def translate_bouten(l)
		if l =~ /［＃「.+?」に傍点］/
			l.gsub!(/(.+?)［＃.*?「\1」に傍点］/, "\\#{BoutenType['傍点']}{\\1}")
		end
		if l =~ /［＃「.+?」に白ゴマ傍点］/
			l.gsub!(/(.+?)［＃.*?「\1」に白ゴマ傍点］/, "\\#{BoutenType['白ゴマ傍点']}{\\1}")
		end
		if l =~ /［＃「.+?」に丸傍点］/
			l.gsub!(/(.+?)［＃.*?「\1」に丸傍点］/, "\\#{BoutenType['丸傍点']}{\\1}")
		end
		if l =~ /［＃「.+?」に白丸傍点］/
			l.gsub!(/(.+?)［＃.*?「\1」に白丸傍点］/, "\\#{BoutenType['白丸傍点']}{\\1}")
		end
		if l =~ /［＃「.+?」に黒三角傍点］/
			l.gsub!(/(.+?)［＃.*?「\1」に黒三角傍点］/, "\\#{BoutenType['黒三角傍点']}{\\1}")
		end
		if l =~ /［＃「.+?」に白三角傍点］/
			l.gsub!(/(.+?)［＃.*?「\1」に白三角傍点］/, "\\#{BoutenType['白三角傍点']}{\\1}")
		end
